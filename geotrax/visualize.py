"""
visualize.py - Video Visualization Tool

This script visualizes tracking results by overlaying them on the original video frames.
It supports various visualization options, including showing bounding boxes, class labels, tracking lines, confidence values,
speed estimates, and lane numbers. The script can also save the annotated video to a file or display it in real-time.
It is designed to work with the results generated by the extraction stage ('geotrax extract'), which processes
video files and generates tracking data in a specific format. If georeferenced results are available, the script can also visualize speed estimates and lane numbers.

Usage:
  geotrax visualize <source> [options]

Arguments:
  source : Path to the input video file.

Options:
  --help, -h          : Show this help message and exit.
  --cfg, -c <path>    : Path to a custom pipeline config file. Defaults to the bundled config;
                        run 'geotrax config show' to view it or 'geotrax config copy' to customize.
  --output-folder, -of <str> : Root folder for outputs (bare name or absolute path).
                        Defaults to cfg -> output -> folder (historical default: 'results').
  --model, -m <str>   : Detection model used to resolve vehicle class names — a local file path
                        OR an 'hf://<org>/<name>/<file>.pt' Hugging Face reference. Only needed
                        when class names are not set via --class-names or cfg -> extraction ->
                        class_rename. Defaults to cfg -> extraction -> model.
  --class-names, -cn <ID=NAME|FILE> [...] : Class-id -> name mapping: a .yaml/.json file or
                        inline ID=NAME pairs (e.g. -cn 0=car 1=bus). Overrides model-derived
                        names. Defaults to cfg -> extraction -> class_rename, then model names.
  --log-path, -lp <str> : Where to write logs: a directory or a full file path; defaults to a platform-specific log directory.
  --verbose, -v       : Set print verbosity level to INFO (default: WARNING).

Visualization Options:
  --save / --no-save, -s  : Save the annotated output video to file. Defaults to cfg -> visualization -> save.
  --show / --no-show, -sh : Open a live preview window during processing. Defaults to cfg -> visualization -> show.
  --viz-mode, -vm <int> [<int> ...] : Frame source(s) for annotation: 0=original, 1=stabilized,
                        2=reference frame. Accepts multiple values (e.g. 0 1 2) to render one
                        video per mode. Defaults to cfg -> visualization -> viz_mode.
  --plot-trajectories, -pt : Overlay trajectory positions on the first frame.
                        Defaults to cfg -> visualization -> plot_trajectories.
  --plot-delay, -pd <int> : Number of frames to display the trajectory overlay; only relevant when
                        --plot-trajectories is enabled. Defaults to cfg -> visualization -> plot_delay.
  --show-conf, -sc    : Include detection confidence in bounding-box labels. Defaults to cfg -> visualization -> show_conf.
  --show-lanes, -sl   : Include lane ID in bounding-box labels. Defaults to cfg -> visualization -> show_lanes.
  --show-class-names, -scn : Include vehicle class name in bounding-box labels. Defaults to cfg -> visualization -> show_class_names.
  --hide-labels, -hl  : Suppress all label text overlays. Defaults to cfg -> visualization -> hide_labels.
  --hide-tracks, -ht  : Suppress track tail lines. Defaults to cfg -> visualization -> hide_tracks.
  --hide-speed, -hs   : Suppress speed values in labels. Defaults to cfg -> visualization -> hide_speed.
  --speed-unit, -su <choice> : Speed display unit: km/h or mi/h. Defaults to cfg -> visualization -> speed_unit.
  --speed-deadzone, -sdz <float> : Floor displayed speeds <= this value (in the chosen speed unit) to 0; 0 disables. Defaults to cfg -> visualization -> speed_deadzone.
  --class-filter, -cf <int> [<int> ...] : Vehicle class IDs to exclude (e.g., -cf 1 2). Defaults to cfg -> visualization -> class_filter.
  --cut-frame-left, -cfl <int> : Skip the first N frames. Defaults to cfg -> processing -> cut_frame_left.
  --cut-frame-right, -cfr <int> : Stop processing after this frame. Defaults to cfg -> processing -> cut_frame_right.

Examples:
1. Visualize the tracking results on a video using the default settings:
   geotrax visualize path/to/video.mp4 --show

2. Save with confidence values and exclude class 0 (default: car):
   geotrax visualize path/to/video.mp4 --save --class-filter 0 --show-conf

3. Hide labels, trajectory trails, and exclude multiple classes:
   geotrax visualize path/to/video.mp4 --show --hide-labels --hide-tracks --class-filter 1 2

4. Save with class names and cut off the first 100 frames:
   geotrax visualize path/to/video.mp4 --save --show-class-names --cut-frame-left 100

5. Visualize tracking results on a stabilized video without speed estimates:
   geotrax visualize path/to/video.mp4 --show --viz-mode 1 --hide-speed

Notes:
- The script reads tracking results from '<stem><tracks_postfix>.txt' in the output folder (default: 'results/').
  Output folder and postfixes are configured via cfg -> output; use --output-folder to redirect at runtime.
- For --viz-mode 1 or 2, transformation matrices are read from '<stem><stab_transform_postfix>.txt' in the same folder.
- Press 'q' during visualization to stop (with --show).
"""

