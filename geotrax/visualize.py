def annotate_frame(frame: np.ndarray, frame_num: int, tracks_frame: pd.DataFrame, track_history: dict, class_names: dict, speed_lane_frame: pd.DataFrame, viz_config: dict, args: argparse.Namespace, logger: logging.Logger, Hinv: np.ndarray = None) -> np.ndarray:
    """
    Annotate the frame with the tracking results.

    For the oriented box modes (3 and 4), ``tracks_frame`` carries the slim oriented layout
    (cols 2/3 = stabilized center, 4/5 = best-fit length/width, 8 = heading, 9 = is_fallback,
    10/11 = stabilized detection bbox extents, 12 = on_border) and ``Hinv`` projects each box from
    stabilized space onto the rendered frame: the inverse stabilization homography for mode 3
    (original frame), or the identity for mode 4 (already-stabilized frame). Rows where col 9 is
    ``True`` used raw bbox dimensions and are drawn with a dashed outline; rows where col 12 is
    ``True`` (detection touches a frame edge) are clipped to cols 10/11 so the box ends at the
    visible part of the vehicle — both handled by :func:`draw_oriented_box`.
    """
    tail_length = viz_config['tail_length']
    line_width = viz_config['line_width']
    colors = VizColors()
    annotated_frame = frame.copy()
    if tracks_frame.empty:
        logger.warning(f"No detection results for frame {frame_num:05d}")
        return annotated_frame

    is_oriented = args.viz_mode in (3, 4)
    ids = tracks_frame.iloc[:, 1].values
    Xcn, Ycn, Wn, Hn = tracks_frame.iloc[:, 2:6].values.T
    classes = tracks_frame.iloc[:, 6].values
    if is_oriented:
        scores = tracks_frame.iloc[:, 7].values
        headings = tracks_frame.iloc[:, 8].values
        fallbacks = tracks_frame.iloc[:, 9].values
        clip_ws = tracks_frame.iloc[:, 10].values
        clip_hs = tracks_frame.iloc[:, 11].values
        on_borders = tracks_frame.iloc[:, 12].values
        interp_flags = [False] * len(ids)   # already encoded in fallbacks for oriented mode
    else:
        scores = tracks_frame.iloc[:, 7].values if tracks_frame.shape[1] >= 8 else [''] * len(ids)
        headings = [None] * len(ids)
        fallbacks = [False] * len(ids)
        clip_ws = [None] * len(ids)
        clip_hs = [None] * len(ids)
        on_borders = [False] * len(ids)
        # shape 9 = stab+interp (is_interpolated at col 8); shape 11 = no-stab+interp (col 10)
        if tracks_frame.shape[1] == 9:
            interp_flags = tracks_frame.iloc[:, 8].values.astype(bool)
        elif tracks_frame.shape[1] == 11:
            interp_flags = tracks_frame.iloc[:, 10].values.astype(bool)
        else:
            interp_flags = [False] * len(ids)

    for track_id, xcn, ycn, wn, hn, c, s, heading, is_fb, clip_w, clip_h, on_border, is_interp in zip(
        ids, Xcn, Ycn, Wn, Hn, classes, scores, headings, fallbacks, clip_ws, clip_hs, on_borders, interp_flags
    ):
        if args.class_filter and c in args.class_filter:
            continue

        speed, lane = None, None
        if speed_lane_frame is not None and int(track_id) in speed_lane_frame.index:
            vehicle_data = speed_lane_frame.loc[int(track_id)]
            if isinstance(vehicle_data, pd.DataFrame):  # duplicate Vehicle_ID in this frame
                vehicle_data = vehicle_data.iloc[0]
            speed = vehicle_data['Vehicle_Speed']
            lane = vehicle_data['Lane_Number']
            # Convert speed for visualization if mi/h is selected (data is stored in km/h)
            if not np.isnan(speed):
                if args.speed_unit == 'mi/h':
                    speed = int(speed * 0.621371)  # Convert km/h to mi/h
                else:
                    speed = int(speed)
                if speed <= args.speed_deadzone:  # Deadzone: floor near-stationary speeds to 0 (display only)
                    speed = 0
            else:
                speed = None
            lane = int(lane) if lane not in ('', None) and pd.notna(lane) else None

        color = colors(c, True)
        if is_oriented:
            # wn/hn carry the best-fit length/width; project the oriented box onto the original frame.
            # For edge-touching detections, clip the box to the visible footprint (clip_w x clip_h).
            x1n, y1n = draw_oriented_box(annotated_frame, xcn, ycn, wn, hn, heading, Hinv, color, line_width, is_fb,
                                         clip_w, clip_h, on_border)
            cx_draw, cy_draw = x1n, y1n
        else:
            x1n, y1n = int(xcn - wn / 2), int(ycn - hn / 2)
            x2n, y2n = int(xcn + wn / 2), int(ycn + hn / 2)
            if is_interp:
                corners = np.array([[x1n, y1n], [x2n, y1n], [x2n, y2n], [x1n, y2n]], dtype=np.int32)
                _draw_dashed_poly(annotated_frame, corners, color, line_width)
            else:
                cv2.rectangle(annotated_frame, (x1n, y1n), (x2n, y2n), color, line_width, lineType=cv2.LINE_AA)
            cx_draw, cy_draw = xcn, ycn

        if not args.hide_labels:
            label_parts = []
            if track_id not in {None, -1}:
                label_parts.append(f'id:{track_id}')
            if args.show_class_names:
                label_parts.append(class_names[c])
            if not args.hide_speed and speed is not None:
                label_parts.append(f'{speed} {args.speed_unit}')
            if args.show_lanes and lane is not None:
                label_parts.append(f'L{lane}')
            if args.show_conf and s != '':
                label_parts.append(f'{s:.2f}')
            label = ' '.join(label_parts)

            tf = max(line_width - 1, 1)
            twn, thn = cv2.getTextSize(label, 0, fontScale=line_width / 3, thickness=tf)[0]
            outside = y1n - thn >= 3
            xt2n = x1n + twn
            yt2n = y1n - thn - 3 if outside else y1n + thn + 3
            cv2.rectangle(annotated_frame, (x1n, y1n), (xt2n, yt2n), color, -1, cv2.LINE_AA)
            cv2.putText(annotated_frame, label, (x1n, y1n - 2 if outside else y1n + thn + 2),
                        0, line_width / 3, colors.txt_color, thickness=tf, lineType=cv2.LINE_AA)

        if not args.hide_tracks:
            track = track_history[track_id]
            track.append((float(cx_draw), float(cy_draw)))
            if len(track) > tail_length:
                track.pop(0)
            points = np.array(track, dtype=np.int32).reshape((-1, 1, 2))
            track_len = len(points)
            for i, point in enumerate(points):
                cv2.circle(annotated_frame, tuple(point[0]), int(1 + 8 * (i + 1) / track_len), color, line_width)
    return annotated_frame


def _draw_dashed_poly(frame: np.ndarray, corners: np.ndarray, color: tuple, thickness: int, dash: int = 10, gap: int = 5) -> None:
    """
    Draw a closed dashed polygon on ``frame`` by rendering each edge as alternating dash/gap segments.

    OpenCV has no native dashed-line API, so this walks each edge at pixel-level steps and draws
    only the ``dash``-length segments, leaving ``gap``-length gaps between them.
    """
    n = len(corners)
    for i in range(n):
        p1 = corners[i].astype(float)
        p2 = corners[(i + 1) % n].astype(float)
        dist = float(np.hypot(*(p2 - p1)))
        if dist < 1:
            continue
        direction = (p2 - p1) / dist
        t = 0.0
        while t < dist:
            t1 = min(t + dash, dist)
            a = (p1 + direction * t).astype(np.int32)
            b = (p1 + direction * t1).astype(np.int32)
            cv2.line(frame, tuple(a), tuple(b), color, thickness, cv2.LINE_AA)
            t += dash + gap


def _clip_poly_to_rect(corners: np.ndarray, xmin: float, ymin: float, xmax: float, ymax: float) -> np.ndarray:
    """
    Clip a convex polygon to an axis-aligned rectangle (Sutherland-Hodgman) and return the result.

    Used by :func:`draw_oriented_box` to trim a full-size oriented box to the visible detection
    footprint when a vehicle is only partially in frame. ``corners`` is an ``Nx2`` float array;
    a convex polygon in yields a convex polygon out (no holes). Returns the clipped vertices as an
    ``Mx2`` float array, which is empty when the polygon lies entirely outside the rectangle.
    """
    # Each edge is a (inside-test, intersection-param) against one rectangle boundary.
    edges = (
        ('x', xmin, 1),   # keep x >= xmin
        ('x', xmax, -1),  # keep x <= xmax
        ('y', ymin, 1),   # keep y >= ymin
        ('y', ymax, -1),  # keep y <= ymax
    )
    poly = [np.asarray(c, dtype=float) for c in corners]
    for axis, bound, sign in edges:
        if not poly:
            break
        ai = 0 if axis == 'x' else 1
        inside = lambda p: sign * (p[ai] - bound) >= 0  # noqa: E731
        clipped = []
        n = len(poly)
        for i in range(n):
            cur, prv = poly[i], poly[(i - 1) % n]
            cur_in, prv_in = inside(cur), inside(prv)
            if cur_in:
                if not prv_in:
                    clipped.append(_segment_axis_intersection(prv, cur, ai, bound))
                clipped.append(cur)
            elif prv_in:
                clipped.append(_segment_axis_intersection(prv, cur, ai, bound))
        poly = clipped
    return np.array(poly, dtype=np.float32) if poly else np.empty((0, 2), dtype=np.float32)


def _segment_axis_intersection(p0: np.ndarray, p1: np.ndarray, axis: int, bound: float) -> np.ndarray:
    """Intersection of segment ``p0``->``p1`` with the line ``coord[axis] == bound`` (assumed to cross it)."""
    denom = p1[axis] - p0[axis]
    t = 0.0 if denom == 0 else (bound - p0[axis]) / denom
    return p0 + t * (p1 - p0)


def _clip_segment_to_rect(p0: np.ndarray, p1: np.ndarray, xmin: float, ymin: float, xmax: float, ymax: float):
    """
    Clip the segment ``p0``->``p1`` to an axis-aligned rectangle (Liang-Barsky).

    Returns the clipped ``(q0, q1)`` endpoints as float arrays, or ``None`` if the segment lies
    entirely outside the rectangle. Used to trim the heading tick so it does not shoot across empty
    road from a (clipped-away, off-frame) box center.
    """
    p0 = np.asarray(p0, dtype=float)
    d = np.asarray(p1, dtype=float) - p0
    t0, t1 = 0.0, 1.0
    clip = ((-d[0], p0[0] - xmin), (d[0], xmax - p0[0]), (-d[1], p0[1] - ymin), (d[1], ymax - p0[1]))
    for pi, qi in clip:
        if pi == 0:
            if qi < 0:
                return None  # parallel to this edge and outside it
            continue
        t = qi / pi
        if pi < 0:
            t0 = max(t0, t)
        else:
            t1 = min(t1, t)
        if t0 > t1:
            return None
    return p0 + t0 * d, p0 + t1 * d


def draw_oriented_box(frame: np.ndarray, cx: float, cy: float, length: float, width: float, heading: float, Hinv: np.ndarray, color: tuple, line_width: int, is_fallback: bool = False, clip_w: float = None, clip_h: float = None, on_border: bool = False) -> tuple:
    """
    Draw a rotated bounding box (visualization modes 3 and 4) and return its projected center.

    The box is built in stabilized image space (center ``cx, cy``; size ``length`` x ``width``;
    rotated to ``heading`` radians) and projected onto the rendered frame via ``Hinv`` (the inverse
    stabilization homography for mode 3, or the identity for mode 4), then drawn as a closed polygon
    with a short tick from the center toward the front edge to indicate travel direction. A NaN
    ``heading`` (no reliable motion) falls back to an axis-aligned box.

    When ``is_fallback`` is ``True`` the box dimensions come from the raw YOLO bounding box rather
    than the best-fit size estimator; in that case the outline is drawn dashed (via
    :func:`_draw_dashed_poly`) so viewers can immediately distinguish estimated from fallback boxes.

    When ``on_border`` is ``True`` the vehicle is only partially in frame, so the full-size box would
    overhang the edge and cover empty road. The box (and its heading tick) is then clipped — in
    stabilized space, before projection — to the visible detection footprint ``clip_w`` x ``clip_h``
    centered on ``cx, cy``. Note this footprint is the axis-aligned (HBB) detection box while the drawn
    box is rotated to the heading, so the clip boundary only approximates where the rotated vehicle
    actually leaves the frame; the clipped edge can be slightly off.
    """
    if heading is None or np.isnan(heading):
        ux, uy = 1.0, 0.0  # axis-aligned fallback when the heading is unknown
    else:
        ux, uy = np.cos(heading), np.sin(heading)
    vx, vy = -uy, ux  # width axis, perpendicular to the heading
    hl, hw = length / 2.0, width / 2.0

    # Corners (front-left, front-right, rear-right, rear-left); front-edge midpoint + center separately.
    corners = np.array([
        [cx + hl * ux - hw * vx, cy + hl * uy - hw * vy],
        [cx + hl * ux + hw * vx, cy + hl * uy + hw * vy],
        [cx - hl * ux + hw * vx, cy - hl * uy + hw * vy],
        [cx - hl * ux - hw * vx, cy - hl * uy - hw * vy],
    ], dtype=np.float32)
    front = np.array([cx + hl * ux, cy + hl * uy], dtype=np.float32)
    center = np.array([cx, cy], dtype=np.float32)
    tick = (center, front)

    if on_border and clip_w is not None and clip_h is not None:
        # Trim to the visible footprint; the detection only sees part of the vehicle at the edge.
        xmin, ymin = cx - clip_w / 2.0, cy - clip_h / 2.0
        xmax, ymax = cx + clip_w / 2.0, cy + clip_h / 2.0
        clipped = _clip_poly_to_rect(corners, xmin, ymin, xmax, ymax)
        if len(clipped) >= 3:  # keep the unclipped box if clipping degenerates
            corners = clipped
        tick = _clip_segment_to_rect(center, front, xmin, ymin, xmax, ymax)

    corners_proj = cv2.perspectiveTransform(corners.reshape(-1, 1, 2), Hinv).reshape(-1, 2).astype(np.int32)
    center_proj = cv2.perspectiveTransform(center.reshape(-1, 1, 2), Hinv).reshape(2).astype(np.int32)

    if is_fallback:
        _draw_dashed_poly(frame, corners_proj, color, line_width)
    else:
        cv2.polylines(frame, [corners_proj.reshape(-1, 1, 2)], isClosed=True, color=color, thickness=line_width, lineType=cv2.LINE_AA)
    if tick is not None:
        tick_proj = cv2.perspectiveTransform(np.array(tick, dtype=np.float32).reshape(-1, 1, 2), Hinv).reshape(-1, 2).astype(np.int32)
        cv2.line(frame, tuple(tick_proj[0]), tuple(tick_proj[1]), color, line_width, lineType=cv2.LINE_AA)
    return int(center_proj[0]), int(center_proj[1])


def display_frame(annotated_frame: np.ndarray, frame_num: int, logger: logging.Logger) -> None:
    """
    Display the annotated frame.
    """
    cv2.putText(annotated_frame, f'Frame {frame_num:05d}', org=(20, 50),
                    fontFace=cv2.FONT_HERSHEY_SIMPLEX, fontScale=1.5, thickness=2, color=(0, 255, 100))
    cv2.putText(annotated_frame, '(Press <q> to stop)', org=(375, 50),
        fontFace=cv2.FONT_HERSHEY_SIMPLEX, fontScale=1.5, thickness=2, color=(0, 125, 255))
    cv2.imshow("YOLOv8 Tracking", annotated_frame)

    if cv2.waitKey(1) == ord('q'):
        logger.warning('Visualization interrupted by user.')
        raise KeyboardInterrupt


def save_frame(vid_writer: cv2.VideoWriter, annotated_frame: np.ndarray, logger: logging.Logger) -> None:
    """
    Save the annotated frame to the video file.
    """
    try:
        vid_writer.write(annotated_frame)
    except cv2.error as e:
        logger.error(f'Failed to write frame to video: {e}')
        sys.exit(1)


def finalize_video(vid_reader: cv2.VideoCapture, vid_writer: cv2.VideoWriter, pbar: tqdm, frame_num: int, show: bool, logger: logging.Logger) -> None:
    """
    Finalize the video processing.
    """
    vid_reader.release()
    if vid_writer is not None:
        vid_writer.release()
        logger.info('Visualization video saved successfully')
    if show:
        cv2.destroyAllWindows()
    pbar.total = frame_num + 1
    pbar.n = frame_num + 1
    pbar.refresh()
    pbar.set_postfix_str('done')
    pbar.close()


def add_visualization_args(group, include_frame_range: bool = True) -> None:
    """
    Register the shared visualization CLI flags on the given argparse parser/group.

    Used by both ``geotrax visualize`` and ``geotrax batch`` so the two commands expose an
    identical set of visualization options (single source of truth, no drift). Every flag
    defaults to ``None`` and is backfilled from the config in ``visualize_results``.

    ``include_frame_range`` adds ``--cut-frame-left``/``--cut-frame-right``; batch sets it to
    ``False`` since it registers those under its processing options instead.
    """
    group.add_argument('--save', '-s', action=argparse.BooleanOptionalAction, default=None,
                       help='Save the annotated output video to file. Defaults to cfg -> visualization -> save.')
    group.add_argument('--show', '-sh', action=argparse.BooleanOptionalAction, default=None,
                       help='Open a live preview window during processing. Defaults to cfg -> visualization -> show.')
    group.add_argument('--viz-mode', '-vm', type=int, nargs='+', default=None, choices=[0, 1, 2, 3, 4], metavar='MODE',
                       help='Frame source(s) for annotation: 0=original, 1=stabilized, 2=reference frame, 3=rotated boxes on the original frame, 4=rotated boxes on the stabilized frame (3 and 4 rotate each box to the vehicle\'s per-frame heading; both require stabilization). Accepts multiple values, e.g. "--viz-mode 0 1 4" renders one video per mode. Defaults to cfg -> visualization -> viz_mode.')
    group.add_argument('--plot-trajectories', '-pt', action=argparse.BooleanOptionalAction, default=None,
                       help='Overlay trajectory positions on the first frame. Defaults to cfg -> visualization -> plot_trajectories.')
    group.add_argument('--plot-delay', '-pd', type=int, default=None,
                       help='Number of frames to display the trajectory overlay; only relevant when --plot-trajectories is enabled. Defaults to cfg -> visualization -> plot_delay.')
    group.add_argument('--show-conf', '-sc', action=argparse.BooleanOptionalAction, default=None,
                       help='Include detection confidence in bounding-box labels. Defaults to cfg -> visualization -> show_conf.')
    group.add_argument('--show-lanes', '-sl', action=argparse.BooleanOptionalAction, default=None,
                       help='Include lane ID in bounding-box labels. Defaults to cfg -> visualization -> show_lanes.')
    group.add_argument('--show-class-names', '-scn', action=argparse.BooleanOptionalAction, default=None,
                       help='Include vehicle class name in bounding-box labels. Defaults to cfg -> visualization -> show_class_names.')
    group.add_argument('--hide-labels', '-hl', action=argparse.BooleanOptionalAction, default=None,
                       help='Suppress all label text overlays. Defaults to cfg -> visualization -> hide_labels.')
    group.add_argument('--hide-tracks', '-ht', action=argparse.BooleanOptionalAction, default=None,
                       help='Suppress track tail lines. Defaults to cfg -> visualization -> hide_tracks.')
    group.add_argument('--hide-speed', '-hs', action=argparse.BooleanOptionalAction, default=None,
                       help='Suppress speed values in labels. Defaults to cfg -> visualization -> hide_speed.')
    group.add_argument('--speed-unit', '-su', type=str, default=None, choices=['km/h', 'mi/h'],
                       help='Speed display unit: km/h or mi/h. Defaults to cfg -> visualization -> speed_unit.')
    group.add_argument('--speed-deadzone', '-sdz', type=float, default=None,
                       help='Floor displayed speeds at or below this value (in the chosen speed unit) to 0, hiding stationary-vehicle jitter; 0 disables. Defaults to cfg -> visualization -> speed_deadzone.')
    group.add_argument('--class-filter', '-cf', type=int, nargs='+', default=None,
                       help='Vehicle class IDs to exclude from visualization (e.g., -cf 1 2). Defaults to cfg -> visualization -> class_filter.')
    group.add_argument('--tail-length', '-tl', type=int, default=None,
                       help='Number of past positions drawn as a fading track trail [frames]; [1, inf). Defaults to cfg -> visualization -> tail_length.')
    group.add_argument('--line-width', '-lw', type=int, default=None,
                       help='Bounding-box and track stroke width [px]; [1, inf). Defaults to cfg -> visualization -> line_width.')
    group.add_argument('--heading-smoothing', '-hsm', type=int, default=None,
                       help='(modes 3, 4) Gaussian smoothing window [frames] for the per-frame heading; larger = steadier, slower to follow turns. Defaults to cfg -> visualization -> heading_smoothing.')
    group.add_argument('--heading-min-speed', '-hms', type=float, default=None,
                       help='(modes 3, 4) Minimum smoothed pixel speed [px/frame] for a reliable heading; below this the heading is held from the last reliable frame. Defaults to cfg -> visualization -> heading_min_speed.')
    group.add_argument('--edge-clip-margin', '-ecm', type=float, default=None,
                       help='(modes 3, 4) Distance [px] within which a detection box is treated as touching a frame edge, triggering clipping of the oriented box to the visible part (absorbs HBB inaccuracy). Defaults to cfg -> visualization -> edge_clip_margin.')
    group.add_argument('--edge-clip-smoothing', '-ecs', type=float, default=None,
                       help='(modes 3, 4) Gaussian window [frames] for smoothing the clip rectangle of edge-touching boxes; larger = steadier shape as the vehicle exits, 0 disables. Defaults to cfg -> visualization -> edge_clip_smoothing.')
    if include_frame_range:
        group.add_argument('--cut-frame-left', '-cfl', type=int, default=None,
                           help='Skip the first N frames. Defaults to cfg -> processing -> cut_frame_left.')
        group.add_argument('--cut-frame-right', '-cfr', type=int, default=None,
                           help='Stop processing after this frame. Defaults to cfg -> processing -> cut_frame_right.')


def parse_cli_args() -> argparse.Namespace:
    """
    Parse command-line arguments
    """
    parser = argparse.ArgumentParser(description='Tracking Results Visualization')

    parser.add_argument('source', type=Path, help='Path to the input video file.')

    optional = parser.add_argument_group('Optional arguments')
    add_common_args(optional)
    optional.add_argument('--model', '-m', nargs='+', default=None, metavar='MODEL', help="Detection model used to resolve vehicle class names: a local path OR an 'hf://<org>/<repo>/<path/to/file>.pt' reference. Defaults to cfg -> extraction -> model.")
    optional.add_argument('--class-names', '-cn', nargs='+', default=None, metavar='ID=NAME|FILE', help="Class-id -> name mapping: a .yaml/.json file or inline ID=NAME pairs (e.g. -cn 0=car 1=bus). Defaults to cfg -> extraction -> class_rename, then model names.")

    viz = parser.add_argument_group('Visualization arguments')
    add_visualization_args(viz)

    return parser.parse_args()

def main() -> None:
    """
    Command-line entry point.
    """
    args = parse_cli_args()
    logger = setup_logger(__name__, args.verbose, args.log_path)

    visualize_results(args, logger)


if __name__ == '__main__':
    main()
