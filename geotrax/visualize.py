def visualize_results(args: argparse.Namespace, logger: logging.Logger) -> None:
    """
    Visualize the tracking results on a video.
    """
    config = load_config_all(args, logger)['main']
    viz = config['visualization']
    proc = config['processing']
    out_cfg_raw = config.get('output', {})
    backfill_args_from_config(args, {
        'save': viz['save'],
        'show': viz['show'],
        'viz_mode': viz['viz_mode'],
        'plot_trajectories': viz['plot_trajectories'],
        'plot_delay': viz['plot_delay'],
        'show_conf': viz['show_conf'],
        'show_lanes': viz['show_lanes'],
        'show_class_names': viz['show_class_names'],
        'hide_labels': viz['hide_labels'],
        'hide_tracks': viz['hide_tracks'],
        'hide_speed': viz['hide_speed'],
        'speed_unit': viz['speed_unit'],
        'speed_deadzone': viz['speed_deadzone'],
        'class_filter': viz['class_filter'],
        'tail_length': viz['tail_length'],
        'line_width': viz['line_width'],
        'heading_smoothing': viz['heading_smoothing'],
        'heading_min_speed': viz['heading_min_speed'],
        'edge_clip_margin': viz['edge_clip_margin'],
        'edge_clip_smoothing': viz['edge_clip_smoothing'],
        'cut_frame_left': proc['cut_frame_left'],
        'cut_frame_right': proc['cut_frame_right'],
        'output_folder': out_cfg_raw.get('folder', 'results'),
    })
    out_cfg = {**out_cfg_raw, 'folder': args.output_folder}
    if not args.save and not args.show:
        logger.warning("Neither --save nor --show is enabled. Visualization will run but produce no output. "
                       "Set 'save' or 'show' in the config file, or pass --save / --show on the command line.")
    class_names = config['class_names']
    viz_config = config['visualization']
    viz_config['tail_length'] = args.tail_length
    viz_config['line_width'] = args.line_width

    viz_modes = normalize_viz_modes(args.viz_mode, logger)
    for viz_mode in viz_modes:
        args.viz_mode = viz_mode
        tracks_txt_filepath, transforms_filepath, tracks_csv_filepath = get_and_verify_filepaths(args, logger, out_cfg)
        tracks, tracks_plotting = read_tracks(tracks_txt_filepath, class_names, args, logger)
        transforms = read_transforms(transforms_filepath, logger)
        speed_lane_data = read_georeferenced_results(tracks_csv_filepath, tracks, logger)
        vid_reader, vid_writer, pbar = initialize_streams(args, logger, out_cfg)

        frame_num = 0
        try:
            for frame_num, annotated_frame in process_frames(tracks, tracks_plotting, transforms, speed_lane_data, vid_reader, pbar, class_names, viz_config, args, logger):
                if args.show:
                    display_frame(annotated_frame, frame_num, logger)
                if args.save:
                    save_frame(vid_writer, annotated_frame, logger)
        except Exception as e:
            logger.error(f"An error occurred: {e}")
        finally:
            finalize_video(vid_reader, vid_writer, pbar, frame_num, args.show, logger)

    args.viz_mode = viz_modes


def resolve_viz_modes(args: argparse.Namespace, logger: logging.Logger) -> list:
    """
    Return the normalized visualization modes, reading the config default when --viz-mode
    was not given on the command line. Used by callers (e.g. batch_process) that need the
    modes before the full config is otherwise loaded; the resolved value is cached on args.
    """
    if args.viz_mode is None:
        args.viz_mode = load_config(args.cfg, logger)['visualization']['viz_mode']
    return normalize_viz_modes(args.viz_mode, logger)


def normalize_viz_modes(viz_mode, logger: logging.Logger) -> list:
    """
    Coerce a single visualization mode or a list of modes into an ordered, de-duplicated list.
    """
    modes = list(viz_mode) if isinstance(viz_mode, (list, tuple)) else [viz_mode]
    valid_modes = []
    for mode in modes:
        if mode not in (0, 1, 2, 3, 4):
            logger.critical(
                f"Invalid visualization mode '{mode}'. Valid modes are 0 (original), 1 (stabilized), "
                f"2 (reference), 3 (rotated boxes on the original frame), "
                f"4 (rotated boxes on the stabilized frame)."
            )
            sys.exit(1)
        if mode not in valid_modes:
            valid_modes.append(mode)
    if not valid_modes:
        logger.critical("No visualization mode specified.")
        sys.exit(1)
    return valid_modes


def process_frames(tracks: pd.DataFrame, tracks_plotting: pd.DataFrame, transforms: dict, speed_lane_data: pd.DataFrame, cap: cv2.VideoCapture, pbar: tqdm, class_names: dict, viz_config: dict, args: argparse.Namespace, logger: logging.Logger):
    """
    Process the video frames and annotate them with tracking results.
    """
    track_history = defaultdict(list)
    frame_num = 0
    viz_phase = args.plot_trajectories  # 0: normal processing phase, 1: trajectory plotting phase
    trajectory_frame = None
    ref_frame = None

    # Pre-group by frame so each iteration is a dict lookup instead of a full-table scan
    tracks_by_frame = dict(tuple(tracks.groupby(0)))
    if speed_lane_data is not None:
        speed_lane_by_frame = {
            frame_id: group.drop(columns=['Frame_ID']).astype({'Vehicle_ID': int}).set_index('Vehicle_ID')
            for frame_id, group in speed_lane_data.groupby('Frame_ID')
        }
    else:
        speed_lane_by_frame = None
    empty_tracks_frame = tracks.iloc[0:0]

    if viz_phase and tracks_plotting is not None:
        trajectory_frame = plot_trajectories(cap, tracks_plotting, args.cut_frame_left, args.cut_frame_right, viz_config, logger)

    while True:
        if viz_phase:
            # Trajectory plotting phase
            if frame_num < args.plot_delay:
                yield 0, trajectory_frame
                frame_num += 1
                continue
            else:
                cap.set(cv2.CAP_PROP_POS_FRAMES, 0)
                frame_num = 0
                viz_phase = 0
                continue

        # Normal video processing phase
        success, frame = cap.read()
        if not success:
            break

        if frame_num < args.cut_frame_left:
            frame_num += 1
            pbar.update()
            continue
        elif frame_num == args.cut_frame_left:
            ref_frame = frame.copy()
        elif args.cut_frame_right is not None and frame_num >= args.cut_frame_right:
            break

        tracks_frame = tracks_by_frame.get(frame_num, empty_tracks_frame)
        speed_lane_frame = speed_lane_by_frame.get(frame_num) if speed_lane_by_frame is not None else None

        Hinv = None
        if args.viz_mode in (1, 4) and frame_num in transforms:
            # Both modes warp the frame into stabilized space (same as mode 1); mode 4 then draws
            # the oriented box directly there (identity projection — the geometry is already stabilized).
            h, w = frame.shape[:2]
            frame = cv2.warpPerspective(frame, transforms[frame_num], (w, h))
        elif args.viz_mode == 2:
            if ref_frame is not None:
                frame = ref_frame.copy()
        elif args.viz_mode == 3:
            # Oriented boxes are built in stabilized space and projected back onto the original
            # frame; the reference frame (and any missing frame) maps with the identity.
            M = transforms.get(frame_num) if transforms is not None else None
            Hinv = (np.linalg.inv(M) if M is not None else np.eye(3)).astype(np.float32)
        if args.viz_mode == 4:
            Hinv = np.eye(3, dtype=np.float32)

        annotated_frame = annotate_frame(frame, frame_num, tracks_frame, track_history, class_names, speed_lane_frame, viz_config, args, logger, Hinv)
        yield frame_num, annotated_frame

        if args.cut_frame_right is not None and frame_num >= args.cut_frame_right:
            break

        frame_num += 1
        pbar.update()


def get_and_verify_filepaths(args: argparse.Namespace, logger: logging.Logger, output_cfg: dict = None) -> tuple:
    """
    Get and verify the filepaths for the provided video and tracking and georeferenced results.
    """

    video_exists, video_filepath = check_if_results_exist(args.source, 'video')
    if not video_exists:
        logger.critical(f"Video file '{video_filepath}' not found.")
        sys.exit(1)

    tracks_txt_exist, tracks_txt_filepath = check_if_results_exist(args.source, 'processed', output_cfg=output_cfg)
    if not tracks_txt_exist:
        logger.critical(f"Tracking results file '{tracks_txt_filepath}' not found. Make sure you have run the extraction stage ('geotrax extract').")
        sys.exit(1)

    if args.viz_mode in (1, 3, 4):
        transforms_exist, transforms_filepath = check_if_results_exist(args.source, 'video_transformations', output_cfg=output_cfg)
        if not transforms_exist:
            logger.critical(f"Transformation file '{transforms_filepath}' not found. Make sure you have enabled stabilization and run the extraction stage ('geotrax extract').")
            sys.exit(1)
    else:
        transforms_filepath = None

    tracks_csv_exist, tracks_csv_filepath = check_if_results_exist(args.source, 'georeferenced', output_cfg=output_cfg)
    if not tracks_csv_exist:
        logger.warning(f"Georeferenced file '{tracks_csv_filepath}' not found. Speed estimates will not be visualized.")
        tracks_csv_filepath = None

    return tracks_txt_filepath, transforms_filepath, tracks_csv_filepath


def read_tracks(tracks_txt_filepath: Path, class_names: dict, args: argparse.Namespace, logger: logging.Logger) -> tuple:
    """
    Read the tracking results from the text file.
    """
    delimiter = detect_delimiter(tracks_txt_filepath)
    tracks = pd.read_csv(tracks_txt_filepath, header=None, delimiter=delimiter)

    if args.viz_mode in (3, 4):
        return read_tracks_oriented(tracks, tracks_txt_filepath, class_names, args, logger)

    # is_interpolated is always the last column: col 14 (stab, 15-col) or col 10 (no-stab, 11-col)
    if tracks.shape[1] in (11, 15):
        is_interpolated = tracks.iloc[:, -1].values
        tracks = tracks.drop(tracks.columns[-1], axis=1)
    else:
        is_interpolated = None
    if tracks.shape[1] == 10 or tracks.shape[1] >= 14:
        # drop dimension columns — keep only cols 0–11
        tracks = tracks.drop(tracks.columns[12:], axis=1)
    if args.plot_trajectories and tracks.shape[1] < 11:
        logger.error(f"No stabilized bounding boxes found in: '{tracks_txt_filepath}'. Disable the trajectory plotting option or re-run the extraction stage ('geotrax extract').")
        sys.exit(1)
    tracks_plotting = tracks[[0, 6, 7, 10]].copy() if tracks.shape[1] >= 11 else None
    if tracks_plotting is not None:
        tracks_plotting.columns = list(range(tracks_plotting.shape[1]))
    if args.viz_mode > 0:
        if tracks.shape[1] < 11:
            logger.error(f"No stabilized bounding boxes found in: '{tracks_txt_filepath}'. Choose a different visualization mode or re-run the extraction stage ('geotrax extract').")
            sys.exit(1)
        tracks = tracks.drop(tracks.columns[2:6], axis=1)
    elif tracks.shape[1] > 10:
        tracks = tracks.drop(tracks.columns[6:10], axis=1)
    elif tracks.shape[1] < 7:
        logger.error(f"No valid tracking results found in: '{tracks_txt_filepath}'.")
        sys.exit(1)
    tracks.columns = list(range(tracks.shape[1]))
    if is_interpolated is not None:
        tracks[tracks.shape[1]] = is_interpolated

    if len(class_names) < tracks[6].max() + 1:
        logger.error(f"At least {tracks[6].max() + 1} class names must be provided. Current class names defined for the used model are {class_names.values()}.")
        sys.exit(1)

    return tracks, tracks_plotting


def _estimate_fallback_dims(tracks: pd.DataFrame) -> tuple:
    """
    Per-vehicle Q25 size estimates derived solely from raw bbox dimensions (cols 4, 5).

    Used as a fallback when the azimuth-filtered estimator in :func:`extract.estimate_vehicle_dimensions`
    could not produce estimates (NaN in cols 12/13). Mirrors that estimator's approach — length =
    longer bbox side, width = shorter side, aggregated at Q25 across the full track — but without
    azimuth filtering. Returns two Series aligned to ``tracks.index``.
    """
    tmp = pd.DataFrame({
        'l': tracks[[4, 5]].max(axis=1),
        'w': tracks[[4, 5]].min(axis=1),
        'id': tracks[1],
    })
    q25 = tmp.groupby('id')[['l', 'w']].transform(lambda x: np.percentile(x, 25))
    return q25['l'], q25['w']


def read_tracks_oriented(tracks: pd.DataFrame, tracks_txt_filepath: Path, class_names: dict, args: argparse.Namespace, logger: logging.Logger) -> tuple:
    """
    Build the slim per-row layout for the rotated box modes (3 and 4).

    Requires the full stabilized + dimension-estimate layout (14 columns). Emits columns
    ``[frame, id, stab_xc, stab_yc, length, width, class, conf, heading, is_fallback,
    stab_w, stab_h, on_border]`` where ``length`` / ``width`` are the best-fit estimates from
    :func:`extract.estimate_vehicle_dimensions` (cols 12/13), or — when those are NaN — a per-vehicle
    Q25 aggregate of the raw bbox extents from :func:`_estimate_fallback_dims`. ``heading`` is the
    per-frame smoothed travel direction (radians, image coordinates) from :func:`compute_headings`.
    ``is_fallback`` (col 9) is ``True`` for rows where the best-fit estimator yielded no result; those
    boxes are drawn with a dashed outline by :func:`draw_oriented_box` to distinguish them visually.

    ``stab_w`` / ``stab_h`` (cols 10/11) are the stabilized detection bbox extents, per-track Gaussian
    smoothed via :func:`_smooth_clip_dims` to de-jitter the clip, and ``on_border`` (col 12) flags rows
    whose *raw* detection touches a frame edge (within ``edge_clip_margin``, mirroring
    :func:`extract.estimate_vehicle_dimensions`). For those rows a full-size oriented box would overhang
    the frame and cover empty road, since the detection only sees the visible part of the vehicle (its
    center is shifted inward and its extent truncated); :func:`draw_oriented_box` clips the box to
    ``stab_w`` x ``stab_h`` so it ends at the visible footprint.
    """
    if tracks.shape[1] < 14:
        logger.error(
            f"Visualization mode 3 requires stabilized tracks with dimension estimates (14 columns) "
            f"in: '{tracks_txt_filepath}'. Re-run the extraction stage ('geotrax extract') with "
            f"stabilization enabled."
        )
        sys.exit(1)

    tracks_plotting = tracks[[0, 6, 7, 10]].copy()
    tracks_plotting.columns = list(range(tracks_plotting.shape[1]))

    headings = compute_headings(tracks, args.heading_smoothing, args.heading_min_speed, logger)
    # Dimension fallback: where the best-fit estimate is NaN, use a per-vehicle Q25 of raw bbox dims.
    is_fallback = tracks[12].isna()
    is_interpolated_flag = tracks[14].astype(bool) if tracks.shape[1] >= 15 else pd.Series(False, index=tracks.index)
    is_dashed = is_fallback | is_interpolated_flag
    fb_length, fb_width = _estimate_fallback_dims(tracks)
    length = tracks[12].where(~is_fallback, fb_length)
    width = tracks[13].where(~is_fallback, fb_width)

    # Per-row partial-visibility flag: a detection whose RAW bbox (cols 2-5) reaches a frame edge
    # only covers the visible part of the vehicle, so its oriented box must be clipped (see
    # draw_oriented_box). The margin treats the box as edge-touching once it comes within
    # `edge_clip_margin` px of the border, since the YOLO HBB may stop a few pixels short of the
    # true edge rather than reaching it exactly.
    eps = getattr(args, 'edge_clip_margin', 3)
    source = getattr(args, 'source', None)
    w_I, h_I = get_video_dimensions(source) if source is not None else (np.inf, np.inf)
    xc, yc, w, h = tracks[2], tracks[3], tracks[4], tracks[5]
    on_border = (
        (xc - w / 2 <= eps) | (yc - h / 2 <= eps)
        | (xc + w / 2 >= w_I - 1 - eps) | (yc + h / 2 >= h_I - 1 - eps)
    )

    oriented = pd.DataFrame({
        0: tracks[0],              # frame id
        1: tracks[1],              # vehicle id
        2: tracks[6],              # stabilized center x
        3: tracks[7],              # stabilized center y
        4: length,                 # best-fit length [px]
        5: width,                  # best-fit width [px]
        6: tracks[10],             # class id
        7: tracks[11],             # confidence
        8: headings,               # per-frame heading [rad, image coords]; NaN -> axis-aligned box
        9: is_dashed.astype(bool),    # True = dashed outline (fallback dims or interpolated row)
        10: tracks[8],             # stabilized bbox width [px]  (clip rectangle)
        11: tracks[9],             # stabilized bbox height [px] (clip rectangle)
        12: on_border.astype(bool),   # True = detection touches a frame edge -> clip to visible part
    })

    # De-jitter the clip rectangle. The oriented box and the clip share a center, so the box's shape
    # only changes with the clip *size* (cols 10/11); smoothing that per-track removes the frame-to-
    # frame detection noise that makes a partially-visible box jump between shapes, while preserving
    # the genuine shrink as the vehicle leaves the frame.
    oriented[[10, 11]] = _smooth_clip_dims(oriented, getattr(args, 'edge_clip_smoothing', 5))

    if len(class_names) < oriented[6].max() + 1:
        logger.error(f"At least {oriented[6].max() + 1} class names must be provided. Current class names defined for the used model are {class_names.values()}.")
        sys.exit(1)

    return oriented, tracks_plotting


def compute_headings(tracks: pd.DataFrame, smoothing: float, min_speed: float, logger: logging.Logger) -> pd.Series:
    """
    Per-frame vehicle heading (radians, image coordinates, y-down) for the oriented box modes (3, 4).

    For each track, the stabilized center trajectory (columns 6, 7) is differentiated and the
    velocity components are smoothed with a Gaussian filter; the heading is ``arctan2(dy, dx)``.
    Frames whose smoothed pixel speed is below ``min_speed`` are unreliable and inherit the
    nearest reliable heading (held, never interpolated across the angle wraparound). A track that
    never moves enough (common in short clips) has no motion direction, so its heading falls back
    to the orientation of the longer raw bounding-box side (cols 4, 5): a detection taller than it
    is wide implies a vertically oriented vehicle (heading pi/2), otherwise horizontal (0). Returns
    a Series aligned to ``tracks.index``.
    """
    headings = pd.Series(np.nan, index=tracks.index, dtype=float)
    sigma = max(float(smoothing), 1e-6)
    for _, grp in tracks.groupby(1):
        grp = grp.sort_values(0)
        # Fallback when motion is unavailable: align the best-fit box with the longer HBB side.
        aspect_fallback = np.pi / 2 if np.median(grp[5]) > np.median(grp[4]) else 0.0
        if len(grp) < 2:
            headings.loc[grp.index] = aspect_fallback
            continue
        x = grp[6].to_numpy(dtype=float)
        y = grp[7].to_numpy(dtype=float)
        dx = gaussian_filter1d(np.gradient(x), sigma, mode='reflect')
        dy = gaussian_filter1d(np.gradient(y), sigma, mode='reflect')
        reliable = np.hypot(dx, dy) >= min_speed
        if not reliable.any():
            headings.loc[grp.index] = aspect_fallback  # stationary: orient by bbox aspect
            continue
        theta = np.where(reliable, np.arctan2(dy, dx), np.nan)
        theta = pd.Series(theta).ffill().bfill().to_numpy()  # hold heading over unreliable frames
        headings.loc[grp.index] = theta
    return headings


def _smooth_clip_dims(oriented: pd.DataFrame, smoothing: float) -> pd.DataFrame:
    """
    Per-track Gaussian smoothing of the clip-rectangle extents (cols 10/11) for the oriented modes.

    The clip rectangle that trims a partially-visible box to its visible footprint is the per-frame
    stabilized detection bbox, which is noisy frame-to-frame. Smoothing its width/height along each
    track (Gaussian, ``mode='reflect'``) removes that jitter so the rendered box changes shape
    smoothly as the vehicle exits the frame, instead of jumping between shapes; a constant series is
    returned unchanged. Returns a 2-column DataFrame (cols 10/11) aligned to ``oriented.index``.
    """
    sigma = max(float(smoothing), 1e-6)
    out = oriented[[10, 11]].astype(float).copy()
    for _, grp in oriented.groupby(1):
        grp = grp.sort_values(0)
        out.loc[grp.index, 10] = gaussian_filter1d(grp[10].to_numpy(dtype=float), sigma, mode='reflect')
        out.loc[grp.index, 11] = gaussian_filter1d(grp[11].to_numpy(dtype=float), sigma, mode='reflect')
    return out


def read_transforms(transforms_filepath: Path, logger: logging.Logger) -> dict:
    """
    Read the transformation matrices from the text file.
    """
    if transforms_filepath is None:
        return None

    delimiter = detect_delimiter(transforms_filepath)
    transforms = np.loadtxt(transforms_filepath, delimiter=delimiter)

    if transforms.ndim == 1:
        transforms = transforms.reshape(1, -1)
    if transforms.shape[1] != 10:
        logger.error(f"Not valid transforms in: '{transforms_filepath}'.")
        sys.exit(1)

    frame_nums = transforms[:, 0].astype(int)
    matrices = transforms[:, 1:].reshape((-1, 3, 3))

    if not np.all(np.diff(frame_nums) == 1):
        logger.warning(f"Missing frame ids found in: '{transforms_filepath}'.")

    if not np.all(np.linalg.det(matrices) > 0):
        logger.error(f"Not valid transforms found in: '{transforms_filepath}'.")
        sys.exit(1)

    transforms = dict(zip(frame_nums, matrices))
    return transforms


def read_georeferenced_results(tracks_csv_filepath: Path, tracks: pd.DataFrame, logger: logging.Logger) -> pd.DataFrame:
    """
    Read the georeferenced tracking results from the CSV file.
    """
    if tracks_csv_filepath is None:
        return None

    georeferenced_data = pd.read_csv(tracks_csv_filepath)
    if 'Frame_Number' in georeferenced_data.columns:
        georeferenced_data.rename(columns={'Frame_Number': 'Frame_ID'}, inplace=True)
    elif 'Timestamp' in georeferenced_data.columns:
        start_frame = int(tracks[0].min())
        csv_timestamps = sorted(georeferenced_data['Timestamp'].unique())
        ts_to_frame = {ts: start_frame + i for i, ts in enumerate(csv_timestamps)}
        georeferenced_data['Frame_ID'] = georeferenced_data['Timestamp'].map(ts_to_frame)
        logger.warning(
            f"'Frame_Number' column missing from '{tracks_csv_filepath.name}'. "
            f"Frame IDs reconstructed from tracking results assuming no dropped frames. "
            f"Re-run 'geotrax georeference' to regenerate the CSV with proper frame numbers."
        )
    else:
        logger.warning(
            f"Neither 'Frame_Number' nor 'Timestamp' column found in '{tracks_csv_filepath.name}'. "
            f"Speed/lane data cannot be displayed. Re-run 'geotrax georeference' to regenerate the CSV."
        )
        return None
    georeferenced_data = georeferenced_data[['Frame_ID', 'Vehicle_ID', 'Vehicle_Speed', 'Lane_Number']]
    return georeferenced_data


def initialize_streams(args: argparse.Namespace, logger: logging.Logger, output_cfg: dict = None) -> tuple:
    """
    Initialize video reader, writer, and progress bar.
    """
    vid_reader = cv2.VideoCapture(str(args.source))
    if not vid_reader.isOpened():
        logger.error(f"Failed to open: '{args.source}'.")
        sys.exit(1)

    frame_count = int(vid_reader.get(cv2.CAP_PROP_FRAME_COUNT))
    if args.save:
        frame_width = int(vid_reader.get(cv2.CAP_PROP_FRAME_WIDTH))
        frame_height = int(vid_reader.get(cv2.CAP_PROP_FRAME_HEIGHT))
        fps = vid_reader.get(cv2.CAP_PROP_FPS) # int() might be required, floats might produce error in MP4 codec

        suffix, fourcc = determine_suffix_and_fourcc()
        out_path = build_result_path(args.source, 'visualized', output_cfg, args.viz_mode, suffix)
        get_output_dir(args.source, output_cfg).mkdir(parents=True, exist_ok=True)
